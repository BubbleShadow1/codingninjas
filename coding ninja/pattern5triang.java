public class pattern5triang {
    public static void main(String arg[])
    {
        for(int i=1;i<=4;i++)
        {
            int j=0;
            while(j<i)
            {
                System.out.printf("%d",j+1);
                j++;
            }
            System.out.print("\n");
        
        }
    }
    
}
