public class pattern4 {
    public static void main(String arg[])
    {
        int i=4;
        while(i>0)
        {
            int j=4;
            while(j>0)
            {
                System.out.printf("%d",j);
                j-- ;
            }
            System.out.printf("\n");
            i--;
        }
    }
    
}
